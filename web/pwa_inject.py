"""
web/pwa_inject.py — Post-processes the pygbag build output to add PWA support.

Run after building with pygbag:
    python -m pygbag --build web_main.py
    python web/pwa_inject.py [--version <tag>]

What it does:
  1. Generates icon-192.png and icon-512.png from assets/icon/icon.png
  2. Copies web/manifest.json into build/web/
  3. Generates a service-worker.js that pre-caches every file in build/web/
     (so the app works offline after the first visit)
  4. Patches build/web/index.html to register the manifest and service worker
"""
from __future__ import annotations

import argparse
import json
import shutil
import sys
from pathlib import Path

ROOT      = Path(__file__).resolve().parent.parent
BUILD_DIR = ROOT / "build" / "web"
WEB_DIR   = ROOT / "web"
ICON_SRC  = ROOT / "assets" / "icon" / "icon.png"


# ── icon generation ───────────────────────────────────────────────────────────

def _generate_icons() -> None:
    try:
        from PIL import Image  # type: ignore
    except ImportError:
        print("WARNING: Pillow not installed — skipping icon generation.")
        print("         Install with: pip install Pillow")
        return

    if not ICON_SRC.exists():
        print(f"WARNING: {ICON_SRC} not found — skipping icon generation.")
        return

    try:
        img = Image.open(ICON_SRC).convert("RGBA")
        for size in (192, 512):
            out = BUILD_DIR / f"icon-{size}.png"
            img.resize((size, size), Image.LANCZOS).save(out)
            print(f"  icon-{size}.png")
    except Exception as exc:
        print(f"WARNING: icon generation failed: {exc} — skipping.")


# ── service worker ────────────────────────────────────────────────────────────

def _collect_files() -> list[str]:
    """Return a list of all cache-able paths relative to BUILD_DIR."""
    skip = {"service-worker.js"}
    # Include root URL so navigating to "./' works offline
    files: list[str] = ["./"]
    for f in sorted(BUILD_DIR.rglob("*")):
        if f.is_file() and f.name not in skip:
            rel = f.relative_to(BUILD_DIR)
            files.append("./" + str(rel).replace("\\", "/"))
    return files


def _write_service_worker(files: list[str], version: str) -> None:
    cache_name = f"aquarium98-{version}"

    # Small critical files are pre-cached during SW install (fast, reliable).
    # Large assets (WASM, Python packages) are cached lazily on first fetch so
    # that a single slow/failed file can't block the entire SW installation.
    small_ext  = {".html", ".json", ".png", ".ico", ".css", ".js"}
    precache   = [f for f in files if Path(f).suffix.lower() in small_ext
                  or f == "./"]
    files_json = json.dumps(precache, indent=2)

    sw = f"""\
// Auto-generated by pwa_inject.py — do not edit manually.
// Cache name includes version so stale caches are replaced on each deploy.
const CACHE_NAME = "{cache_name}";

// Small critical files are pre-cached at install time.
// Large assets (WASM, Python packages) are cached lazily on first fetch.
const PRECACHE_FILES = {files_json};

self.addEventListener("install", event => {{
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => cache.addAll(PRECACHE_FILES))
      .then(() => self.skipWaiting())
  );
}});

self.addEventListener("activate", event => {{
  event.waitUntil(
    caches.keys().then(keys =>
      Promise.all(
        keys.filter(k => k !== CACHE_NAME).map(k => caches.delete(k))
      )
    ).then(() => self.clients.claim())
  );
}});

// Cache-first for assets; navigation requests fall back to index.html
// so the app loads correctly offline regardless of the URL entered.
// Large assets not in PRECACHE_FILES are cached lazily on first fetch.
self.addEventListener("fetch", event => {{
  if (event.request.mode === "navigate") {{
    event.respondWith(
      caches.match("./index.html").then(r => r || fetch(event.request))
    );
    return;
  }}
  event.respondWith(
    caches.match(event.request).then(cached => {{
      if (cached) return cached;
      return fetch(event.request).then(response => {{
        if (response && response.ok) {{
          const cloned = response.clone();
          caches.open(CACHE_NAME).then(cache => cache.put(event.request, cloned));
        }}
        return response;
      }});
    }})
  );
}});
"""
    (BUILD_DIR / "service-worker.js").write_text(sw, encoding="utf-8")
    print(f"  service-worker.js  (cache: {cache_name}, {len(precache)} pre-cached + {len(files) - len(precache)} lazy)")


# ── index.html patch ──────────────────────────────────────────────────────────

_PWA_HEAD = """\
  <link rel="manifest" href="manifest.json">
  <meta name="theme-color" content="#000080">
  <script>
    if ("serviceWorker" in navigator) {
      window.addEventListener("load", () => {
        navigator.serviceWorker.register("service-worker.js")
          .then(r  => console.log("[SW] registered:", r.scope))
          .catch(e => console.warn("[SW] registration failed:", e));
      });
    }
  </script>"""


def _patch_html() -> None:
    html_path = BUILD_DIR / "index.html"
    if not html_path.exists():
        print("WARNING: build/web/index.html not found — skipping HTML patch.")
        return
    html = html_path.read_text(encoding="utf-8")
    if '<link rel="manifest"' in html:
        print("  index.html already patched — skipping.")
        return
    html = html.replace("</head>", _PWA_HEAD + "\n</head>", 1)
    html_path.write_text(html, encoding="utf-8")
    print("  index.html  (manifest + SW registration injected)")


# ── main ──────────────────────────────────────────────────────────────────────

def main() -> None:
    parser = argparse.ArgumentParser(description="Inject PWA support into pygbag build output.")
    parser.add_argument("--version", default="dev", help="Version string for the service-worker cache name.")
    args = parser.parse_args()

    if not BUILD_DIR.exists():
        print(f"ERROR: {BUILD_DIR} not found.", file=sys.stderr)
        print("       Run 'python -m pygbag --build web_main.py' first.", file=sys.stderr)
        sys.exit(1)

    print(f"Injecting PWA support into {BUILD_DIR}/")

    print("Generating icons ...")
    _generate_icons()

    print("Copying manifest.json ...")
    shutil.copy(WEB_DIR / "manifest.json", BUILD_DIR / "manifest.json")
    print("  manifest.json")

    print("Writing service worker ...")
    files = _collect_files()
    _write_service_worker(files, args.version)

    print("Patching index.html ...")
    _patch_html()

    print("Done — PWA support added.")
    print()
    print("After deploying, users can click 'Install' in Chrome/Edge to add")
    print("Aquarium 98 as a standalone offline desktop/mobile app.")


if __name__ == "__main__":
    main()
